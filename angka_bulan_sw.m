Bulan = input('Masukkan Bulan (1-12) = ');

switch Bulan
    
case 1
disp('Januari')

case 2
disp('Februari')

case 3
disp('Maret')

case 4
disp('April')
 
 case 5
disp('Mei')

case 6
disp('Juni')
 
case 7
disp('Juli')
 
case 8
disp('Agustus')
 
case 9
disp('September')
 
case 10
disp('Oktober')
 
case 11
disp('November')
 
case 12
disp('Desember')
 
otherwise

disp('*Kamu Hanya Bisa Memilih Nilai 1-12')
disp('Jalankan Ulang Kode')
end